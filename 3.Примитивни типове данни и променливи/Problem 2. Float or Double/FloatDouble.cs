using System;

namespace Problem_2.Float_or_Double
{
    class FloatDouble
    {
        static void Main(string[] args)
        {
            double number_1 = 34.567839023d;
            float number_2 = 12.345f;
            double number_3 = 8923.1234857;
            float number_4 = 3456.091f;

            Console.WriteLine("{0}\n{1}\n{2}\n{3}", number_1, number_2, number_3, number_4);
        }
    }
}
